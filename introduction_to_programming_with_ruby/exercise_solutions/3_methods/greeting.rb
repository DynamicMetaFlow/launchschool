def greeting()
  name = gets.chomp.capitalize.to_s
  puts "Rise and shine, Mister #{name}. Rise and... shine. Not that I... wish to imply you have been sleeping on the job. No one is more deserving of a rest, and all the effort in the world would have gone to waste until... well, let's just say your hour has... come again. The right man in the wrong place can make all the difference in the world. So, wake up, Mister #{name}. Wake up and... smell the ashes..."
end

puts greeting
